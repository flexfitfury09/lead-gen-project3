def process_webhook_event(user_id: int, event: dict):
    etype = str(event.get('type','')).lower()
    email = event.get('email') or event.get('recipient') or ''
    if not email:
        return 'ignored: no email'
    if etype in ('bounce','bounced','complaint','spamreport','spam_report'):
        _suppress_email_for_user(user_id, email)
        _mark_email_bounced(email)
        return f"suppressed:{etype}"
    return 'processed'

def authenticate_user(username_or_email, password):
    """Authenticate user login by username or email"""
    conn = sqlite3.connect('lead_gen.db')
    cursor = conn.cursor()
    
    username_or_email = (username_or_email or '').strip()
    password_hash = hash_password(password)
    cursor.execute('''
        SELECT id, username, email, role FROM users
        WHERE ((username = ?) OR (LOWER(email) = LOWER(?))) AND password_hash = ?
    ''', (username_or_email, username_or_email, password_hash))
    
    user = cursor.fetchone()
    conn.close()
    
    if user:
        return {
            'id': user[0],
            'username': user[1],
            'email': user[2],
            'role': user[3]
        }
    # If not found, ensure admin exists and try once more
    try:
        ensure_admin_exists()
        cursor = conn.cursor()
        cursor.execute('''
            SELECT id, username, email, role FROM users
            WHERE ((username = ?) OR (LOWER(email) = LOWER(?))) AND password_hash = ?
        ''', (username_or_email, username_or_email, password_hash))
        user = cursor.fetchone()
        if user:
            return {
                'id': user[0],
                'username': user[1],
                'email': user[2],
                'role': user[3]
            }
    except Exception:
        pass
    return None

